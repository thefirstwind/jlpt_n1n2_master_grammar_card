def normalize_qobj(qobj: dict) -> tuple[str, list[tuple[str, str]]]:
    stem = qobj.get("stem", "")
    opts = qobj.get("opts") or []
    if opts:
        return stem, [(o[0], o[1]) for o in opts]
    pairs = STEM_OPTS.findall(stem)
    if pairs:
        base = re.split(r"\s+[abc]\s+", stem)[0].strip()
        return base, [(a, b.strip()) for a, b in pairs]
    return stem, []


def extract_rule(body: str) -> str:
    parts = []
    if "接续：" in body:
        parts.append(body.split("接续：")[1].split("注意：")[0].strip().replace("\n", " "))
    if "注意：" in body:
        parts.append(body.split("注意：")[1].strip().replace("\n", " ")[:220])
    if not parts and "意味：" in body:
        parts.append(body.split("意味：")[1].split("\n")[0].strip())
    return "；".join(p for p in parts if p)


def parse_md(text: str) -> dict:
    result: dict = {}
    cur_anchor: str | None = None
    cur_q: str | None = None
    in_table = False

    for raw in text.splitlines():
        line = raw.strip()
        m = ANCHOR_RE.match(line)
        if m:
            cur_anchor = m.group(1)
            result[cur_anchor] = {"rule": "", "questions": {}}
            cur_q = None
            in_table = False
            continue
        if not cur_anchor:
            continue
        rm = RULE_RE.match(line)
        if rm:
            result[cur_anchor]["rule"] = rm.group(1).strip()
            continue
        qm = Q_RE.match(line)
        if qm:
            cur_q = qm.group(1)
            result[cur_anchor]["questions"][cur_q] = {"answer": "", "options": {}}
            in_table = False
            continue
        if cur_q and line.startswith("|") and "选项" in line:
            in_table = True
            continue
        if cur_q and in_table and line.startswith("|") and not line.startswith("|---"):
            row = TABLE_ROW.match(line)
            if not row:
                continue
            letter, text_opt, reason = row.group(1), row.group(2).strip(), row.group(3).strip()
            verdict = "correct" if "**" in row.group(2) else "wrong"
            reason = re.sub(r"^\*\*|\*\*$", "", reason).strip()
            if not reason:
                reason = "符合语法点。" if verdict == "correct" else "不合本语法点或语境。"
            result[cur_anchor]["questions"][cur_q]["options"][letter] = {
                "verdict": verdict,
                "reason": reason,
            }
            continue
        am = ANS_RE.match(line)
        if am and cur_q:
            result[cur_anchor]["questions"][cur_q]["answer"] = am.group(1)
            in_table = False
    return result


