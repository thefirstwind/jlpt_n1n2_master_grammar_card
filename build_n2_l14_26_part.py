def gen_reason(
    aid: str,
    q: str,
    letter: str,
    text: str,
    correct: str,
    pattern: str,
    body: str,
    stem: str,
) -> tuple[str, str]:
    key = f"{aid}|{q}"
    if key in HAND_NOTES and letter in HAND_NOTES[key]:
        verdict = "correct" if letter == correct else "wrong"
        return verdict, HAND_NOTES[key][letter]
    is_correct = letter == correct
    verdict = "correct" if is_correct else "wrong"
    if is_correct:
        return verdict, f"代入题干后「{text}」与「{pattern.split('（')[0]}」的接续、语义一致，故为正确答案。"
    return verdict, f"「{text}」代入后接续不当或与题干逻辑、本语法点的注意点不符，故排除。"


def main() -> None:
    export = json.loads(EXPORT.read_text(encoding="utf-8"))
    answers = json.loads(ANSWERS.read_text(encoding="utf-8"))
    md_parsed = parse_md(MD.read_text(encoding="utf-8"))
    out: dict = {}

    for aid in sorted(export):
        if not LESSON_RE.match(aid):
            continue
        card = export[aid]
        body = card.get("body", "")
        pattern = card.get("pattern", "")
        rule = md_parsed.get(aid, {}).get("rule") or extract_rule(body)
        out[aid] = {"rule": rule, "questions": {}}
        ans_map = answers.get(aid, {})

        for qobj in card["questions"]:
            q = str(qobj["q"])
            stem, opts = normalize_qobj(qobj)
            correct = ans_map.get(q, "").lower()
            md_q = (md_parsed.get(aid, {}).get("questions") or {}).get(q, {})
            qentry = {"answer": correct, "options": {}}

            for letter, text in opts:
                md_opt = (md_q.get("options") or {}).get(letter, {})
                if md_opt.get("reason"):
                    reason = md_opt["reason"]
                    verdict = "correct" if letter == correct else "wrong"
                else:
                    verdict, reason = gen_reason(
                        aid, q, letter, text, correct, pattern, body, stem
                    )
                qentry["options"][letter] = {"verdict": verdict, "reason": reason}
            out[aid]["questions"][q] = qentry

    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(json.dumps(out, ensure_ascii=False, indent=2), encoding="utf-8")
    n_q = sum(len(v["questions"]) for v in out.values())
    print(f"Wrote {OUT.name}: {len(out)} anchors, {n_q} questions")


if __name__ == "__main__":
    main()
